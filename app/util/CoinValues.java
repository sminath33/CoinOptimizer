package app.util;

public class CoinValues {
    private String silverDollar;
    private String halfDollar;
    private String quarter;
    private String dime;
    private String nickel;
    private String penny;
    
    public void setSilverDollar(String sd)
    {
        this.silverDollar = sd;
    }
    
    public String getSilverDollar()
    {
        return this.silverDollar;
    }
    
    public void setHalfDollar(String hd)
    {
        this.halfDollar = hd;
    }
    
    public String getHalfDollar()
    {
        return this.halfDollar;
    }
    
    public void setQuarter(String q)
    {
        this.quarter = q;
    }
    
    public String getQuarter()
    {
        return this.quarter;
    }
    
    public void setDime(String d)
    {
        this.dime = d;
    }
    
    public String getDime()
    {
        return this.dime;
    }
    
    public void setNickel(String n)
    {
        this.nickel = n;
    }
    
    public String getNickel()
    {
        return this.nickel;
    }
    
    public void setPenny(String p)
    {
        this.penny = p;
    }
    
    public String getPenny()
    {
        return this.penny;
    }
    
}